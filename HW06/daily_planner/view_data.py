def planner_view(data):
    d = ['Понедельник:', 'Вторник:', 'Среда:', 'Четверг:',
         'Пятница:', 'Суббота:', 'Воскресенье:']
    dd = ['Понедельник:\n', 'Вторник:\n', 'Среда:\n', 'Четверг:\n', 'Пятница:\n', 'Суббота:\n', 'Воскресенье:\n']

    for o in range(len(d)):
        print(d[o])
        for i in range(len(data[dd[o]])):
            print(i + 1, data[dd[o]][i], end='')
        print()
    _ = input('Введите любой символ для возвращения в меню: ')
    return data
